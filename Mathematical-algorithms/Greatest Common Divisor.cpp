//Euclidean algorithm for finding GCD(Greatest Common Divisor) of two numbers
//O(log max(a,b))
//Vladimir "VladaMG98" Milenkovic
#include <bits/stdc++.h>
using namespace std;
int gcd(int a,int b)
{
    if(b==0) return a;
    return gcd(b,a%b);
}
//Testing
int main()
{
    int a=10;
    int b=15;
    printf("%d\n",gcd(a,b));//Prints 5
    return 0;
}
